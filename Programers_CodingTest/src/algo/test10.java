package algo;

public class test10 {

	public static void main(String[] args) {
		int x = 130;
		System.out.println(x%10);
		
	}
	
    public static boolean solution(int x) {
        boolean answer = true;
        //x 나눠서 합침
        while(true) {
        	if(x < 10) {
        		break;
        	}
        }
        //x를 나눠진 자릿수의 합이 나머지가 0이면 true 
        return answer;
    }
    
 

}
